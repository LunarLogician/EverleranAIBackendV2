// Shuffle options and update correctAnswer index so the correct answer
// is not always in the same position (Claude tends to put it at index 0).
function shuffleOptions(questions) {
  return questions.map(q => {
    // Normalize correctAnswer to an index first
    let correctIdx = 0;
    const ca = q.correctAnswer;
    if (typeof ca === 'number') {
      correctIdx = ca;
    } else if (typeof ca === 'string') {
      const upper = ca.trim().toUpperCase();
      if (['A', 'B', 'C', 'D'].includes(upper)) {
        correctIdx = upper.charCodeAt(0) - 65;
      } else {
        const found = q.options.findIndex(
          o => o.trim().toLowerCase() === ca.trim().toLowerCase()
        );
        correctIdx = found >= 0 ? found : 0;
      }
    }

    // Fisher-Yates shuffle on a copy of options
    const opts = [...q.options];
    for (let i = opts.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [opts[i], opts[j]] = [opts[j], opts[i]];
    }

    // Find where the correct answer ended up after shuffle
    const newCorrectIdx = opts.indexOf(q.options[correctIdx]);

    return { ...q, options: opts, correctAnswer: newCorrectIdx };
  });
}

// Generate quiz
exports.generateQuiz = async (req, res, next) => {
  try {
    const { documentId, quizTitle, numberOfQuestions: _rawQ = 10 } = req.body;
    const userId = req.user._id;
    const numberOfQuestions = Math.min(Math.max(parseInt(_rawQ, 10) || 10, MIN_GENERATED), MAX_GENERATED);

    if (!documentId || !isValidObjectId(documentId)) {
      return res.status(400).json({ message: 'Valid documentId is required' });
    }

    const document = await Document.findById(documentId);
    if (!document || document.userId.toString() !== userId.toString()) {
      return res.status(403).json({ message: 'Unauthorized access' });
    }

    // Call Claude to generate quiz
    const prompt = `Generate exactly ${numberOfQuestions} multiple choice questions from this document. Return JSON format with array of objects containing "question", "options" (array of 4 strings), "correctAnswer" (the 0-based index of the correct option — vary this across questions, do NOT always use 0), and "explanation" keys. Only return valid JSON array.\n\nDocument:\n${document.textContent}`;

    const claudeMessages = [
      {
        role: 'user',
        content: prompt,
      },
    ];

    const claudeResponse = await callClaude(claudeMessages, 'quiz', 2048);

    // Parse quiz from Claude response
    let questionsData = [];
    try {
      const jsonMatch = claudeResponse.content.match(/\[[\s\S]*\]/);
      questionsData = JSON.parse(jsonMatch ? jsonMatch[0] : claudeResponse.content);
    } catch (parseError) {
      questionsData = [
        {
          question: 'Sample Question?',
          options: ['Option A', 'Option B', 'Option C', 'Option D'],
          correctAnswer: 0,
          explanation: 'AI parsing failed',
        },
      ];
    }
    questionsData = shuffleOptions(questionsData);

    // Create quiz
    const quiz = new Quiz({
      userId,
      documentId,
      quizTitle,
      questions: questionsData,
      totalQuestions: questionsData.length,
      autoGeneratedTokens: claudeResponse.outputTokens,
    });

    await quiz.save();

    // Update usage
    const usage = await Usage.findOne({ userId });
    if (usage) {
      usage.inputTokens += claudeResponse.inputTokens;
      usage.outputTokens += claudeResponse.outputTokens;
      usage.totalTokens = usage.inputTokens + usage.outputTokens;

      const featureUsage = usage.usageBreakdown.find((u) => u.featureName === 'quiz');
      if (featureUsage) {
        featureUsage.inputTokens += claudeResponse.inputTokens;
        featureUsage.outputTokens += claudeResponse.outputTokens;
        featureUsage.count += 1;
      } else {
        usage.usageBreakdown.push({
          featureName: 'quiz',
          inputTokens: claudeResponse.inputTokens,
          outputTokens: claudeResponse.outputTokens,
          count: 1,
        });
      }

      await usage.save();
    }

    res.status(201).json({
      success: true,
      quiz: {
        id: quiz._id,
        quizTitle: quiz.quizTitle,
        totalQuestions: quiz.totalQuestions,
        questions: questionsData,
      },
      tokensUsed: {
        input: claudeResponse.inputTokens,
        output: claudeResponse.outputTokens,
      },
    });
  } catch (error) {
    next(error);
  }
};

