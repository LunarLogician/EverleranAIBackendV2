// Submit quiz attempt
exports.submitQuizAttempt = async (req, res, next) => {
  try {
    const { quizId } = req.params;
    const { answers } = req.body; // Array of selected answers
    const userId = req.user._id;

    // Validate answers array
    if (!answers || !Array.isArray(answers)) {
      return res.status(400).json({
        message: 'Invalid request. Provide answers array: { "answers": ["answerA", "answerB", ...] }'
      });
    }

    const quiz = await Quiz.findById(quizId);
    if (!quiz || quiz.userId.toString() !== userId.toString()) {
      return res.status(403).json({ message: 'Unauthorized access' });
    }

    // Calculate score
    let score = 0;
    const feedback = [];

    quiz.questions.forEach((question, index) => {
      const isCorrect = answers[index] === question.correctAnswer;
      if (isCorrect) score += 1;

      feedback.push({
        questionIndex: index,
        isCorrect,
        explanation: question.explanation,
      });
    });

    // Get AI feedback on attempt
    const feedbackPrompt = `Review this quiz attempt and provide encouraging feedback. Score: ${score}/${quiz.questions.length} (${Math.round((score / quiz.questions.length) * 100)}%)`;

    const claudeMessages = [
      {
        role: 'user',
        content: feedbackPrompt,
      },
    ];

    const claudeResponse = await callClaude(claudeMessages, 'qa', 512);

    // Save attempt
    const attempt = {
      userId,
      score,
      totalMarks: quiz.questions.length,
      answers,
      feedback: claudeResponse.content,
      attemptedAt: new Date(),
    };

    quiz.attempts.push(attempt);
    await quiz.save();

    // Update usage
    const usage = await Usage.findOne({ userId });
    if (usage) {
      usage.outputTokens += claudeResponse.outputTokens;
      usage.totalTokens = usage.inputTokens + usage.outputTokens;
      await usage.save();
    }

    res.status(200).json({
      success: true,
      result: {
        score,
        totalMarks: quiz.questions.length,
        percentage: Math.round((score / quiz.questions.length) * 100),
        feedback: claudeResponse.content,
        details: feedback,
      },
    });
  } catch (error) {
    next(error);
  }
};

exports.deleteQuiz = async (req, res, next) => {
  try {
    const { quizId } = req.params;
    const quiz = await Quiz.findById(quizId);
    if (!quiz || quiz.userId.toString() !== req.user._id.toString()) {
      return res.status(403).json({ message: 'Unauthorized access' });
    }
    await Quiz.findByIdAndDelete(quizId);
    res.status(200).json({ success: true, message: 'Quiz deleted' });
  } catch (error) {
    next(error);
  }
};
